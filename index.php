  <p>
    တောင်ပေါ်မြေပြန့်အနှံ့မှ မြန်မာလူငယ်များနှင့် ကိုက်ညီမည့် အသိပညာ၊ အတတ်ပညာမျိုးစုံပေးနိုင်မည့် သင်ခန်းစာများ ဤနေရာတွင် ပေးဝေနိုင်ရင် ပြင်ဆင်ထားပါသည်။ မြန်မာ့ဒီမိုကရေစီလှုပ်ရှားမှုနှင့် တစ်သားတည်းရှိမှုကို ပြသည့်အနေနှင့် ကမ္ဘာအနှံ့မှ ပညာရှင်များ၊ စာရေးဆရာများ၊ အတွေးအခေါ်ရှင်များက ဤနေရာမှ စာသင်ကြားကြပါမည်။ မိုဘိုင်းဖုန်းပေါ်သို့ ဒေါင်းလုတ်လုပ်နိုင်မည့် အသံ၊ အရုပ်၊ စာကို အခြေခံမည့် အွန်လိုင်းသင်ခန်းစာမျိုးစုံကို ဖန်တီးပေးနိုင်ရန် မျှော်လင့်ပါသည်။ မြန်မာပြည်တွင်းမှ ကျောင်းသားများနှင့်ပြည်သူများအတွက် သင်ခန်းစာများကို ရေဒီယိုနှင့် တီဗီမှတဆင့် ထုတ်လွှင့်ပါမည်။ လွတ်မြောက်နယ်မြေများဖြစ်သည့် ကချင်၊ ကရင်၊ မွန်၊ ရှမ်း၊ကရင်နီနှင့် ရန်ကုန်၊ မန္တလေး စသည့်မြို့များတွင် ရှိသော ကိုယ်ပိုင်အုပ်ချုပ်ခွင့်ရတက္ကသိုလ်များနှင့် ပူးပေါင်းကာ ကျောင်းသားအချင်းချင်း သင်ကြားမျှဝေရေး အစီအစဉ်များကိုလည်း လုပ်ဆောင်သွားမည် ဖြစ်ပါသည်။
